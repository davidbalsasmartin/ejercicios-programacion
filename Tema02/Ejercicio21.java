public class Ejercicio21 {
  public static void main(String[] args) {

    int x = 144;
    int y = 999;

    int sum = x + y;
    System.out.println("La suma es " + sum);
    
    int mul = x * y;
    System.out.println("La multiplicación es " + mul);
    
    int div = y / x;
    System.out.println("La división de 999 entre 144 es" + div);
    
    int res = x - y;
    System.out.println("La resta de 144 menos 999 es" + res);
  }
}
